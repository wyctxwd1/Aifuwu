public class Uses {
    private String Name;
    private int ImgId;

    public Uses(String name, int imgId) {
        Name = name;
        ImgId = imgId;
    }

    public String getName() {
        return Name;
    }

    public int getImgId() {
        return ImgId;
    }
}
